            var msg = "Hi!" + " " + emp.FullName + " " + "Your Inventory Management  Account Has been Created. And Your Login Credential is Email: " + emp.Email + " " + "and Password: " + emp.Password;

            var bodyBuilder = new BodyBuilder { HtmlBody = string.Format("<h2 style='color:red;'>{0}</h2>", msg) };

            //if (mailRequest.Attachments != null)
